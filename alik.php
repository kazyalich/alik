<?php
// Получаем возраст от пользователя
$age = intval(readline('Введите ваш возраст: '));

// Определяем этап тренировок
if ($age < 18) {
    echo "Вы находитесь на этапе начинающего спортсмена.";
} elseif ($age >= 18 && $age < 30) {
    echo "Вы находитесь на этапе спортсмена со средним уровнем подготовки.";
} else {
    echo "Вы находитесь на этапе профессионального спортсмена.";
}
?>
